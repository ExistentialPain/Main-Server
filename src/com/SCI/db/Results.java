package com.SCI.db;

import java.sql.ResultSet;
import java.sql.ResultSetMetaData;
import java.sql.SQLException;
import java.util.ArrayList;

public class Results {
    Results(ResultSet res, ResultSet keys) throws SQLException {
        if (res == null) {
            System.out.println("res is null");
        }
        if (keys == null) {
            System.out.println("keys is null");
        }
        results = new ArrayList<>();
        autoGeneratedKeys = new ArrayList<>();
        if (res != null) {
            ResultSetMetaData rsmd = res.getMetaData();
            while (res.next()) {
                Row row = new Row();
                results.add(row);
                for (int i = 0; i < rsmd.getColumnCount(); ++i) {
                    row.add(rsmd.getColumnName(i + 1), res.getObject(i + 1));
                }
            }
        }
        if (keys != null) {
            ResultSetMetaData keyData = keys.getMetaData();
            while (keys.next()) {
                Row row = new Row();
                autoGeneratedKeys.add(row);
                for (int i = 0; i < keyData.getColumnCount(); ++i) {
                    row.add(keyData.getColumnName(i + 1), keys.getObject(i + 1));
                }
            }
        }
    }

    public Row getRow(int index) {
        return results.get(index);
    }

    public long getAutoGeneratedKey(int index) {
        return (Long) autoGeneratedKeys.get(index).get("GENERATED_KEY");
    }

    @Override
    public String toString() {
        StringBuilder sb = new StringBuilder();
        sb.append("DATABASE\n");
        for (Row result : results) {
            sb.append(result.toString());
        }
        sb.append("GENERATED KEYS");
        for (Row autoGeneratedKey : autoGeneratedKeys) {
            sb.append(autoGeneratedKey.toString());
        }
        return sb.toString();
    }

    public int length() {
        return results.size();
    }

    private final ArrayList<Row> autoGeneratedKeys;

    private final ArrayList<Row> results;
}
